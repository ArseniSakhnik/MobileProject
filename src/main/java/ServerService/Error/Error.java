package ServerService.Error;

public class Error {
    private int code;
    private String message;

    public int getCode() {
        return code;
    }

    public void setCode(int conde) {
        this.code = conde;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }


}
